# numero entero -> Integer : int 
numero = 123
print(numero)
print(type(numero))

# numero decimal -> Float : float
decimal = 12.45
print(decimal)
print(type(decimal))

# text -> String : str
texto = "lo que sea " 
print(texto)
print(type(texto))

# nulo -> None : NoneType
nulo = None
print(nulo)
print(type(nulo))

# booleano -> Boolean : bool
mayoria_de_edad = False
print(type(mayoria_de_edad)) 